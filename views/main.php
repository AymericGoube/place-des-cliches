        <h2 class="titlehistory">Merci pour votre engagement !</h2>

        <ul class="collapsible" data-collapsible="accordion">
            <li>
                <div class="collapsible-header"> Notre histoire </div>
                <div class="collapsible-body">
                    <h1> L’histoire de Place des Clichés </h1>
                    <p> Le projet Place des Clichés est né en 2015 dans le cadre d’un Service Civique au sein de l’association

                    Unis-Cité, plus précisément dans leur programme Rêve et Réalise qui permet aux volontaires de

                    monter leur propre projet social et solidaire. </p>

                    <p class="subtitle"> Est-ce que vous vous êtes déjà demandé pourquoi ce sont très souvent des femmes qui font le

                    ménage dans les pubs pour produits d’entretien ? </p>

                    <p> Place des Clichés part avant tout d’un constat personnel de Fania. Ayant deux petits frères, elle s’est

                    très vite perçu qu’on ne les traitait pas de la même manière (au niveau des tâches ménagères par

                    exemple ou des autorisations de sorite), et que cette différence venait surtout du fait qu’elle soit une

                    fille et eux des garçons plutôt que par rapport à leur différence d’âge. En dehors du cadre familial,

                    dans des séries, publicités etc. elle se rendait compte de la différence de traitement qui était réservé

                    aux femmes et aux hommes. </p>

                    <p> Un exemple qui parle beaucoup, les publicités pour les produits d’entretien, où la plupart du temps, ce

                    sont des femmes qui sont présentées, comme si leur rôle était obligatoirement de faire le ménage. 

                    Bref, des stéréotypes sur les femmes et les hommes, il y en a un peu partout, et on les intériorise tous

                    par des processus de socialisation. </p>

                    <p> Quant à Fania, elle s’est toujours demandée pourquoi c’était comme ça, sans jamais vraiment avoir de

                    réponse mis à part celle du « c’est comme ça » qui ne lui convenait pas tout à fait.

                    C’est après avoir eu des cours de sociologie du genre qu’elle a eu les réponses à ses questions, et

                    qu’elle s’est dit que finalement, il y a plein de jeunes qui ne sont que peu ou pas sensibilisés à ces

                    questions. <p>

                    <p class="subtitle"> Des ateliers auprès des jeunes comme solution </p>

                    <p> La solution qu’elle a voulu apporter à ce problème : faire des ateliers de sensibilisation auprès de

                    jeunes pour qu’ils puissent remettre en question les stéréotypes qu’ils voient et entendent au quotidien

                    et développer l’esprit critique des jeunes. Car c’est en remettant en question des stéréotypes que l’on

                    pourra se rapprocher de l’égalité réelle entre les femmes et les hommes. </p>

                    <p> D’un simple projet de Service Civique lancé en 2015, Place des Clichés a grandit : le projet est devenu

                    une association et se constitue d’une équipe de choc, avec des ateliers co-construits avec des experts ! </p>
                </div>
            </li>
        </ul>
